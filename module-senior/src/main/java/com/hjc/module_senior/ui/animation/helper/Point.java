package com.hjc.module_senior.ui.animation.helper;

/**
 * @Author: HJC
 * @Date: 2021/8/15 15:37
 * @Description: 定义的点
 */
public class Point {

    private float x;
    private float y;

    public Point(float x, float y) {
        this.x = x;
        this.y = y;
    }

    public float getX() {
        return x;
    }

    public void setX(float x) {
        this.x = x;
    }

    public float getY() {
        return y;
    }

    public void setY(float y) {
        this.y = y;
    }
}
